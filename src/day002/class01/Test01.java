package day002.class01;

public class Test01 {
    public static void main(String[] args) {
        // 코딩할 때 자랑하기
        // 자세하고 가독성이 좋은 코드 및 "주석"

        // 변수 3요소
        // 1. 공간 2. 주소 3. 이름
        int num; // 변수를 선언했다. 정의했다.
        num=10; // 초기화했다.

        // 연산자 우선순위 and 디버깅표 작성하기
        int result=++num * num;

        // 산술 대입 복합대입 연산자 및 if문
        System.out.println(num);
        System.out.println(result);
    }
}
